    /**
     * Botão de cadastro que realiza o cadastro.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        /**
         * Se a conexão for bem sucedida.
         */
        try {

            Connection con = LojasEntrega.conector();

            if (txtusuario.getText().trim().isEmpty() || txtsenha.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            } else {
                /**
                 * Insere os valores digitados das caixas de texto no lugar do
                 * ?.
                 */
                String queryCheck = "select * from tbcontas where Usuario=?";
                pst = conexao.prepareStatement(queryCheck);
                pst.setString(1, txtusuario.getText());
                rs = pst.executeQuery();
                String sql = "insert into tbcontas(Usuario, Senha) values (?,?)";

                if (rs.next()) {

                    /**
                     * Se existir usuario e senha correspondentes, será exibido
                     * uma mensagem.
                     */
                    JOptionPane.showMessageDialog(null, "Usuário já está Cadastrado!");

                } else {

                    
                    PreparedStatement stmt = conexao.prepareStatement(sql);
                    
                 
                    /**
                     * Insere os valores no banco de dados.
                     */
                    stmt.setString(1, txtusuario.getText());
                    stmt.setString(2, new String(txtsenha.getPassword()));
                    stmt.execute();
                    stmt.close();

                    /**
                     * Mostra uma mensagem quando um usuario é cadastrado.
                     */
                    JOptionPane.showMessageDialog(null, "Usuário Cadastrado com Sucesso!");

                    /**
                     * Após cadastrar, remove os valores das caixas de texto,
                     * possibilitando outro cadastro.
                     */
                    txtusuario.setText("");
                    txtsenha.setText("");
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

