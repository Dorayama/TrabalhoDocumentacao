    /**
     * metodo onde será feito o Login.
     */
    public void logar() {

        /**
         * Seleciona os dados usuario e senha da tabela tbcontas.
         */
        String sql = "select * from tbcontas where Usuario=? and Senha=?";

        try {
            /**
             * Substitui o ? pelo que foi digitado nas caixas de texto, e checa
             * se esse nome e senha estão cadastrados no banco.
             */
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtusuario.getText());
            pst.setString(2, new String(txtsenha.getPassword()));

            /**
             * Executa a query.
             */
            rs = pst.executeQuery();

            if (rs.next()) {
                /**
                 * Se existir usuario e senha correspondentes, será criado e
                 * exibido o Jframe da tela principal.
                 */
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                this.dispose();
                conexao.close();

            } else {
                /**
                 * se não existir usuario ou senha uma mensagem será mostrada.
                 */
                JOptionPane.showMessageDialog(null, "usuario e/ou senha inválido(s)");
            }
        } catch (Exception e) {
            /**
             * Se a conexão falhar.
             */
            JOptionPane.showMessageDialog(null, e);
        }

    }

